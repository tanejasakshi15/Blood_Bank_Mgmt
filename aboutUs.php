		  A blood bank is a center where blood gathered as a result of blood donation is stored and preserved for later use in blood transfusion. The term "blood bank" typically refers to a division of a hospital where the storage of blood product occurs and where proper testing is performed (to reduce the risk of transfusion related adverse events). However, it sometimes refers to a collection center, and indeed some hospitals also perform collection. A blood collection program was initiated in the US in 1940 and Edwin Cohn pioneered the process of blood fractionation. 
		  <br>He worked out the techniques for isolating the serum albumin fraction of blood plasma, which is essential for maintaining the osmotic pressure in the blood vessels, preventing their collapse.The use of blood plasma as a substitute for whole blood and for transfusion purposes was proposed as early as 1918, in the correspondence columns of the British Medical Journal, by Gordon R. Ward.
		  At the onset of World War II, liquid plasma was used in Britain.
		  <br><br>A large project, known as 'Blood for Britain' began in August 1940 to collect blood in New York City hospitals for the export of plasma to Britain. A dried plasma package was developed, which reduced breakage and made the transportation, packaging, and storage much simpler
		  
		  
		  
